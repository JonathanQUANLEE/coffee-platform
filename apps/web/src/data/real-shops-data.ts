function seededRandom(seed: number): number {
  const x = Math.sin(seed) * 10000;
  return x - Math.floor(x);
}

export function generateReviews(shopId: string, count: number = 50): Review[] {
  const reviews: Review[] = [];
  const shop = allShops.find(s => s.id === shopId);
  if (!shop) return [];

  for (let i = 0; i < count; i++) {
    const seed = shopId.charCodeAt(0) * 1000 + i;
    const rand = seededRandom(seed);
    
    const drinkKeys = Object.keys(reviewContents);
    const drink = drinkKeys[Math.floor(rand * drinkKeys.length)];
    const drinkReviews = reviewContents[drink];
    const reviewTemplate = drinkReviews[Math.floor(seededRandom(seed + 1) * drinkReviews.length)];
    
    const genericTemplate = genericReviews[Math.floor(seededRandom(seed + 2) * genericReviews.length)];
    const activity = activities[Math.floor(seededRandom(seed + 3) * activities.length)];
    const genericContent = genericTemplate.replace('{activity}', activity);
    
    const content = i < 30 ? reviewTemplate : genericContent;
    const source = reviewSources[Math.floor(seededRandom(seed + 4) * reviewSources.length)];
    const username = usernames[Math.floor(seededRandom(seed + 5) * usernames.length)];
    const rating = seededRandom(seed + 6) > 0.15 ? 5 : 4;
    const day = Math.floor(seededRandom(seed + 7) * 28) + 1;
    const month = seededRandom(seed + 8) > 0.3 ? 5 : 4;

    reviews.push({
      id: `${shopId}-r${i}`,
      shopId,
      username,
      rating,
      content,
      source,
      date: `2026-${month.toString().padStart(2, '0')}-${day.toString().padStart(2, '0')}`,
      helpful: Math.floor(seededRandom(seed + 9) * 500) + 10,
    });
  }
  return reviews;
}

export const getReviewsByShopId = (shopId: string): Review[] => generateReviews(shopId, 50);